			<li><strong>Missionen</strong></li>
			<ul>
				<li>Die Missionen dienen zum einen zur Erwirtschaftung von Nuyen, damit ihr euch f&uuml;r sp&auml;tere Missionen bessere Ausrüstung leisten könnt.</li>
				<li>Zum anderen gibt es auch spezielle Plotmissionen f&uuml;r alle, und personalisierte Missionen die nur f&uuml;r euch da sind.</li>
				<li>Sowohl Plot als auch personalisierte Missionen dienen zum Transport der Geschichte im Vorfeld. Außerdem könnt ihr Gegenstände erhalten, die euch später im RPG weiter bringen.</li>
				<li>Auch bieten die speziellen Missionen die Möglichkeit, schon virtuell ein wenig zu Spielen, das ist sogar schon geschehen. ;) </li>
			</ul>
			<br />
			<li><strong>Kampfsystem</strong></li>
			<ul>
				<li>Das Kampfsystem ist KEIN eins zu eins Abbild der tatsächlichen SR 3.01 Regeln! Das System ist viel zu komplex, als das ich eine Chance hätte, es in der Kürze einer Website abbilden zu können. ;)</li>
				<li>Das System basiert allerdings auf den Regeln, und spiegelt diese so gut es geht wieder. Sei es das Initiativesystem, oder das Nahkampfsystem</li>
				<li>Es gibt nur physischen Schaden! Geistigen Schaden gibt es nur beim Entzug von Zaubern.</li>
				<li>Es gibt KEINE Poolwürfel, evtl ändert sich dies aber noch in einer späteren Version.</li>
				<li>Es gibt (noch) KEINE Granaten.</li>
				<li>Das Magiesystem wurde recht rudimentär umgesetzt, und umfasst nur wenige Zauber. Die Kreativität des tatsächlichen Magiesystems lässt sich kaum abbilden.</li>
				<li>Die Händler haben nur eine begrenzte Auswahl an Waren, Nachschub ist nicht in Sicht.</li>
